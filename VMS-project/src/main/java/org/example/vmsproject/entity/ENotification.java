package org.example.vmsproject.entity;

public enum ENotification {
    SYSTEM,    // Thông báo từ hệ thống, ví dụ: bảo trì, cập nhật hệ thống
    USER,      // Thông báo từ người dùng, ví dụ: tin nhắn từ người quản lý
    REMINDER,  // Thông báo nhắc nhở, ví dụ: nhắc nhở về bảo trì hoặc lịch trình
    ALERT      // Cảnh báo khẩn cấp, ví dụ: tai nạn, sự cố trên lộ trình
}
